# Arithmetic Operators

print(2 + 3)
# 5

print(9 - 8)
# 1

print(4 * 6)
# 24

print(8 / 4)
# 2.0

print(5 // 2)
# Floor Division (also called Integer Division) : Quotient when a is divided by b, rounded to the next smallest whole
# number
# 2

print(2 ** 3)  # Exponentiation : a raised to the power of b
# 8

print(10 % 3)
# 1

#######################################################################

# Assignment Operators

x = 2

x += 2
print(x)
# 4

a, b = 5, 6

print(a)
# 5

print(b)
# 6

#######################################################################

# Unary Operators

x = 2

y = -x

print(y)
# -2

#######################################################################

# Relational Operators

a = 5
b = 6

print(a < b)
# True

print(a > b)
# False

print(a == b)
# False

print(a <= b)
# True
print(a >= b)
# False

print(a != b)
# True

#######################################################################

# Logical Operator

a = 5
b = 4

print(a < 8 and b < 5)
# True

print(a < 8 and b < 2)
# False

print(a < 8 or b < 2)
# True

x = True

print(not x)
# False
